package com.nscorp.math;

public class MathOp {
	public static void main(String[] args) {
		System.out.println(Math.max(12, 80));
		System.out.println(Math.sqrt(121));
		System.out.println(Math.min (45698, 325574));
		System.out.println(Math.addExact(568, 2580));
		System.out.println(Math.subtractExact(365, 458));
		System.out.println(Math.absExact(-3651));
		System.out.println(Math.cbrt(8));
		System.out.println(Math.pow(6, 2));
		System.out.println(Math.decrementExact(36));
		System.out.println(Math.incrementExact(3658));
		System.out.println(Math.multiplyExact(3254, 58));
	}

}
